import 'dart:io';

enum TaskStatus { planned, started, completed }

enum TaskType { maintenance, event, inspection, reparation }

enum TaskExecutor { shared, company, user, all }

class Task {
  String? taskId;
  String title;
  DateTime date;
  DateTime? nextDate;
  String? taskInterval;
  TaskExecutor executor;
  String? executorId;
  String userId;
  String? itemId;
  String description;
  String comments;
  TaskStatus status;
  TaskType type;
  List<File>? images;

  Task({
    this.taskId,
    required this.title,
    required this.date,
    this.nextDate,
    this.taskInterval,
    required this.executor,
    this.executorId,
    required this.userId,
    this.itemId,
    required this.description,
    required this.comments,
    required this.status,
    required this.type,
    this.images,
  });

  void setAutogeneratedId(String id) {
    itemId = id;
  }
}
